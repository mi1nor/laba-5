class Employee {
    #name;
    #salary;
    #age;

    setName(name) {
        this.#name = name;
    }

    setSalary(salary) {
        this.#salary = salary;
    }

    setAge(age) {
        this.#age = age;
    }

    getName() {
        this.#name;
    }

    getSalary() {
        this.#salary;
    }

    getAge() {
        this.#age;
    }
}